function toDateString(date: Date) {
  const year = date.getFullYear();
  const month = `${date.getMonth() + 1}`.padStart(2, "0");
  const day = `${date.getDate()}`.padStart(2, "0");
  return `${year}-${month}-${day}`;
}

function toDateTimeString(date: Date) {
  return `${toDateString(date)}T${`${date.getHours()}`.padStart(2, "0")}:${`${date.getMinutes()}`.padStart(2, "0")}:00`;
}

function withOffset(base: Date, days: number, hours = 12, minutes = 0) {
  const next = new Date(base);
  next.setHours(hours, minutes, 0, 0);
  next.setDate(next.getDate() + days);
  return next;
}

function withMinuteDelta(base: Date, minutes: number) {
  const next = new Date(base);
  next.setSeconds(0, 0);
  next.setMinutes(next.getMinutes() + minutes);
  return next;
}

function createLocationId(location: PickupLocation) {
  return `loc_${location.toLowerCase().replace(/\s+/g, "_")}`;
}

